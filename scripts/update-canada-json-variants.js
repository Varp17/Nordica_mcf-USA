
import db from '../config/database.js';

async function update() {
  try {
    const [products] = await db.query("SELECT id, name, sku, color_options FROM products WHERE country IN ('CAD', 'CA')");
    console.log(`Processing variants for ${products.length} Canada products...`);

    for (const p of products) {
      if (!p.sku) continue; // Should have been generated by my previous script
      
      let options = p.color_options;
      if (typeof options === 'string') {
        try { options = JSON.parse(options); } catch (e) { options = null; }
      }

      if (options && Array.isArray(options)) {
        let changed = false;
        for (const opt of options) {
          if (!opt.amazon_sku && !opt.sku) {
            const suffix = opt.value || opt.name || 'DEFAULT';
            const vSku = `${p.sku}-V-${suffix.replace(/\s+/g, '-').toUpperCase()}`;
            opt.amazon_sku = vSku; // Shippo/MCF often looks at amazon_sku
            opt.sku = vSku;
            changed = true;
            console.log(`Generated SKU ${vSku} for ${p.name} variant ${opt.name}`);
          } else if (!opt.sku || !opt.amazon_sku) {
            const existing = opt.sku || opt.amazon_sku;
            opt.sku = existing;
            opt.amazon_sku = existing;
            changed = true;
          }
        }

        if (changed) {
          await db.query("UPDATE products SET color_options = ? WHERE id = ?", [JSON.stringify(options), p.id]);
        }
      }
    }

    console.log("Variant JSON update complete.");
  } catch (err) {
    console.error("Update Error:", err);
  } finally {
    process.exit();
  }
}

update();
